package main

import "fmt"

// value type examples
func main() {

	// string concatenation
	fmt.Println("go" + "lang")

	// showing how integers and floats work
	fmt.Println("1+1=", 1+1)
	fmt.Println("7.0/3.0=", 7.0/3.0)

	// boolean operators
	fmt.Println(true && false)
	fmt.Println(true || false)
	fmt.Println(!true)

}
